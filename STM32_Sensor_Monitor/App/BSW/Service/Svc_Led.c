static Svc_Led_ModeType currentMode = SVC_LED_MODE_OFF;
/*
************************************************************************************************************************
*                                              Private function prototypes
************************************************************************************************************************
*/
/*
************************************************************************************************************************
*                                                    Public functions
************************************************************************************************************************
*/

/**
 * @brief   Set LED operating mode.
 * @param   mode   SVC_LED_MODE_OFF, SVC_LED_MODE_ON, or SVC_LED_MODE_BLINK
 * @retval  None
 */
void Svc_Led_SetMode(Svc_Led_ModeType mode)
{
    currentMode = mode;
}

/**
 * @brief   Periodic LED processing.
 *          Executes LED action based on current mode.
 * @retval  None
 */
void Svc_Led_MainFunction(void)
{
    switch (currentMode)
    {
        case SVC_LED_MODE_ON:
            EcuAbs_Gpio_SetLed();
            break;

        case SVC_LED_MODE_BLINK:
            EcuAbs_Gpio_ToggleLed();
            break;

        case SVC_LED_MODE_OFF:
            EcuAbs_Gpio_ClearLed();
            break;

        default:
            EcuAbs_Gpio_ClearLed();
            break;
    }
}
