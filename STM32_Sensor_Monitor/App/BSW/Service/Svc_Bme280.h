/*
************************************************************************************************************************
*                                                  Include header files
************************************************************************************************************************
*/
#include <stdint.h>
/*
************************************************************************************************************************
*                                                   Defines and macros
************************************************************************************************************************
*/
/*
************************************************************************************************************************
*                                                         Typedefs
************************************************************************************************************************
*/

/** @brief Svc_Bme280 operation status definition */
typedef enum
{
    SVC_BME280_OK           = 0x00U,   /*!< No error                          */
    SVC_BME280_ERROR        = 0x01U,   /*!< General error                     */
    SVC_BME280_COMM_ERROR   = 0x02U,   /*!< I2C communication error           */
    SVC_BME280_CHIP_ID_ERR  = 0x03U    /*!< Chip ID mismatch                  */
} Svc_Bme280_StatusType;
/*
************************************************************************************************************************
*                                                    Exported variables
************************************************************************************************************************
*/
/*
************************************************************************************************************************
*                                               Exported function prototypes
************************************************************************************************************************
*/

/**
 * @brief   Initializes BME280 sensor and verifies Chip ID.
 * @retval  Svc_Bme280 status
 */
Svc_Bme280_StatusType Svc_Bme280_Init(void);

